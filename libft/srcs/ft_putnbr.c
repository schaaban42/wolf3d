#include "libft.h"

void	ft_putnbr(int n)
{
	int		tab[10];
	int		i;

	i = 0;
	while (n != 0 || i == 0)
	{
		tab[i++] = n % 10;
		n = n / 10;
	}
	i = i - 1;
	if (tab[i] < 0)
		ft_putchar('-');
	while (i >= 0)
	{
		ft_putchar(((tab[i] < 0) ? (-tab[i]) : (tab[i])) + '0');
		i--;
	}
}
